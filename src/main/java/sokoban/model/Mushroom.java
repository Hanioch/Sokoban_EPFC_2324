package sokoban.model;

public class Mushroom extends Element {

    private int x, y;
    public Mushroom(int x, int y){

    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }
}
